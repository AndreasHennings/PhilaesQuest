package ur.mi.oop.philae.universe;

/**
 * This enum is used to describe the landers different engine modes: OFF,
 * FIRING UPWARDS (lander is accelerating away from the comet) and 
 * FIRING DOWNWARDS (lander is accelerating towards the comet)
 * 
 * @author Alexander Bazo
 * @version 1.0
 */
public enum LanderMode 
{
	ENGINE_OFF,
	ENGINE_FIRING_UPWARDS,
	ENGINE_FIRING_DOWNWARDS
}
